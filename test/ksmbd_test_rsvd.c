/* ---- Tunnel validation helper ---- */

static int test_validate_tunnel_header(void *in_buf, unsigned int in_buf_len,
				       struct test_tunnel_header **hdr)
{
	if (in_buf_len < TUNNEL_HEADER_SIZE)
		return -EINVAL;

	*hdr = (struct test_tunnel_header *)in_buf;

	if ((le32_to_cpu((*hdr)->OperationCode) & RSVD_TUNNEL_OPCODE_PREFIX_MASK)
	    != RSVD_TUNNEL_OPCODE_PREFIX_VAL)
		return -ENODEV;

	return 0;
}

/* ---- Sub-operation handlers ---- */

static __le32 test_handle_get_initial_info(void *out_buf,
					   unsigned int max_out_len,
					   unsigned int *out_len)
{
	struct test_initial_info_rsp *rsp;

	if (max_out_len < sizeof(*rsp)) {
		*out_len = 0;
		return cpu_to_le32(STATUS_BUFFER_TOO_SMALL);
	}

	rsp = (struct test_initial_info_rsp *)out_buf;
	memset(rsp, 0, sizeof(*rsp));
	rsp->ServerVersion = cpu_to_le32(RSVD_PROTOCOL_VERSION_2);
	rsp->SectorSize = cpu_to_le32(512);
	rsp->PhysicalSectorSize = cpu_to_le32(4096);
	*out_len = sizeof(*rsp);
	return 0;
}

