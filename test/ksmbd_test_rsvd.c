{
	unsigned int max_out = sizeof(struct test_svhd_support) - 1;

	KUNIT_EXPECT_TRUE(test, max_out < sizeof(struct test_svhd_support));
}

static void test_tunnel_header_too_small(struct kunit *test)
{
	u8 small[4];
	struct test_tunnel_header *hdr;
	int ret;

	memset(small, 0, sizeof(small));
	ret = test_validate_tunnel_header(small, sizeof(small), &hdr);
	KUNIT_EXPECT_EQ(test, ret, -EINVAL);
}

static void test_tunnel_invalid_opcode_prefix(struct kunit *test)
{
	struct test_tunnel_header hdr;
	struct test_tunnel_header *out;
	int ret;

	memset(&hdr, 0, sizeof(hdr));
	hdr.OperationCode = cpu_to_le32(0x03001001); /* Wrong prefix: 0x03 */
	ret = test_validate_tunnel_header(&hdr, sizeof(hdr), &out);
	KUNIT_EXPECT_EQ(test, ret, -ENODEV);
}

static void test_tunnel_unknown_version_bits(struct kunit *test)
{
	struct test_tunnel_header rsp_hdr;
	u8 payload[256];
	unsigned int out_len, in_len;
	__le32 rsp_status;
	void *buf;
	int ret;

	buf = build_tunnel_req(test, 0x02003001, 42, &in_len);
	ret = test_dispatch_tunnel(buf, in_len, sizeof(rsp_hdr) + sizeof(payload),
				   &rsp_hdr, payload, &out_len, &rsp_status);
	KUNIT_EXPECT_EQ(test, ret, -EINVAL);
	KUNIT_EXPECT_EQ(test, le32_to_cpu(rsp_status),
			(u32)STATUS_SVHDX_VERSION_MISMATCH);
}

