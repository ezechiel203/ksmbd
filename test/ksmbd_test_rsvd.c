/* ---- Tunnel dispatcher ---- */

static int test_dispatch_tunnel(void *in_buf, unsigned int in_buf_len,
				unsigned int max_out_len,
				struct test_tunnel_header *rsp_hdr,
				void *payload_out,
				unsigned int *out_len,
				__le32 *rsp_status)
{
	struct test_tunnel_header *req_hdr;
	u32 opcode;
	u32 ver_bits;
	unsigned int payload_max;
	unsigned int payload_out_len = 0;
	__le32 tunnel_status;
	int ret;

	ret = test_validate_tunnel_header(in_buf, in_buf_len, &req_hdr);
	if (ret) {
		if (ret == -ENODEV) {
			*rsp_status = cpu_to_le32(STATUS_INVALID_DEVICE_REQUEST);
			return -EINVAL;
		}
		*rsp_status = cpu_to_le32(STATUS_BUFFER_TOO_SMALL);
		return ret;
	}

	opcode = le32_to_cpu(req_hdr->OperationCode);

	ver_bits = opcode & RSVD_TUNNEL_OPCODE_VER_MASK;
	if (ver_bits != RSVD_TUNNEL_OPCODE_VER1 &&
	    ver_bits != RSVD_TUNNEL_OPCODE_VER2) {
		*rsp_status = cpu_to_le32(STATUS_SVHDX_VERSION_MISMATCH);
		return -EINVAL;
	}

	if (max_out_len < TUNNEL_HEADER_SIZE) {
		*rsp_status = cpu_to_le32(STATUS_BUFFER_TOO_SMALL);
		return -ENOSPC;
	}

	rsp_hdr->OperationCode = req_hdr->OperationCode;
	rsp_hdr->RequestId = req_hdr->RequestId;

	payload_max = max_out_len - TUNNEL_HEADER_SIZE;

	switch (opcode) {
	case RSVD_TUNNEL_GET_INITIAL_INFO:
		tunnel_status = test_handle_get_initial_info(
			payload_out, payload_max, &payload_out_len);
		break;
	case RSVD_TUNNEL_CHECK_CONNECTION_STATUS:
		tunnel_status = test_handle_check_connection(&payload_out_len);
		break;
	case RSVD_TUNNEL_GET_DISK_INFO:
		tunnel_status = test_handle_get_disk_info(
			payload_out, payload_max, &payload_out_len);
		break;
	case RSVD_TUNNEL_VALIDATE_DISK:
		tunnel_status = test_handle_validate_disk(
			payload_out, payload_max, &payload_out_len);
		break;
	case RSVD_TUNNEL_SRB_STATUS_OPERATION:
		tunnel_status = test_handle_srb_status(
			payload_out, payload_max, &payload_out_len);
		break;
	case RSVD_TUNNEL_META_OPERATION_QUERY_PROGRESS:
		tunnel_status = test_handle_meta_op_progress(
			payload_out, payload_max, &payload_out_len);
		break;
	case RSVD_TUNNEL_CHANGE_TRACKING_GET_PARAMETERS:
		tunnel_status = test_handle_change_tracking_params(
			payload_out, payload_max, &payload_out_len);
		break;
	case RSVD_TUNNEL_SCSI_OPERATION:
	case RSVD_TUNNEL_META_OPERATION_START:
	case RSVD_TUNNEL_VHDSET_QUERY_INFORMATION:
	case RSVD_TUNNEL_DELETE_SNAPSHOT:
	case RSVD_TUNNEL_CHANGE_TRACKING_START:
	case RSVD_TUNNEL_CHANGE_TRACKING_STOP:
	case RSVD_TUNNEL_QUERY_VIRTUAL_DISK_CHANGES:
	case RSVD_TUNNEL_QUERY_SAFE_SIZE:
		tunnel_status = cpu_to_le32(STATUS_NOT_SUPPORTED);
		payload_out_len = 0;
		break;
	default:
		tunnel_status = cpu_to_le32(STATUS_INVALID_PARAMETER);
		payload_out_len = 0;
		break;
	}

	rsp_hdr->Status = tunnel_status;
	*out_len = TUNNEL_HEADER_SIZE + payload_out_len;
	*rsp_status = 0;
	return 0;
}

/* Helper to build a tunnel request buffer */
static void *build_tunnel_req(struct kunit *test, u32 opcode, u64 req_id,
			      unsigned int *total_len)
{
	struct test_tunnel_header *hdr;

	hdr = kunit_kzalloc(test, TUNNEL_HEADER_SIZE, GFP_KERNEL);
	KUNIT_ASSERT_NOT_NULL(test, hdr);
	hdr->OperationCode = cpu_to_le32(opcode);
	hdr->RequestId = cpu_to_le64(req_id);
	hdr->Status = 0;
	*total_len = TUNNEL_HEADER_SIZE;
	return hdr;
}

