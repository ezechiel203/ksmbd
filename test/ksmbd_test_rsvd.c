static void test_query_svhd_support_normal(struct kunit *test)
{
	struct test_svhd_support support;

	memset(&support, 0, sizeof(support));
	support.SharedVirtualDiskSupport = cpu_to_le32(SVHD_SUPPORT_SHARED);
	support.SharedVirtualDiskHandleState = cpu_to_le32(SVHD_HANDLE_STATE_NONE);

	KUNIT_EXPECT_EQ(test, le32_to_cpu(support.SharedVirtualDiskSupport),
			(u32)SVHD_SUPPORT_SHARED);
}

